 PHONE_NO NUMBER,
 NATIONALITY NUMBER,
 GENDER NUMBER,
 RELIGION NUMBER,
 CREDIT_LIMT NUMBER,
 CREATE_BY VARCHAR2(200),
 CREATE_ON DATE, 
 COMPANY_ID NUMBER,
 ACTIVE NUMBER
)


