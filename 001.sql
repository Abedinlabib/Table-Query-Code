-----BRAND----

CREATE TABLE BRAND(
 ID NUMBER PRIMARY KEY,
 B_NAME VARCHAR2(200),
 ACTIVE NUMBER
)

-------CATEGORY-------

CREATE TABLE CATEGORY(
 ID NUMBER PRIMARY KEY,
 CAT_NAME VARCHAR2(200),
 PID_NUMBER NUMBER,
 CREATE_BY NUMBER,
 CREATE_ON DATE,
 COMPANY_ID NUMBER,
 ACTIVE NUMBER
)

-----CUSTOMER INFO------

